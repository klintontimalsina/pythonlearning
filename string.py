print("Hello")
print('Hello')

# slicing
b = "Hello, World!"
print(b[2:5])

# negative indexing

b = "Hello, World!"
print(b[-5:-2])

# upper case
a = "Hello, World!"
print(a.upper())

# concatenation
a = "Hello"
b = "World"
c = a + b
print(c)

# add space
a = "Hello"
b = "World"
c = a + " " + b
print(c)

# format()
age = 16
txt = "My name is clinton, and I am {}"
print(txt.format(age))